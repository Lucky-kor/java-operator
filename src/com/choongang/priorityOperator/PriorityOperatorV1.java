package com.choongang.priorityOperator;

public class PriorityOperatorV1 {
    public static void main(String[] args) {
        // 연산자 우선순위 예제

        // 1. 곱셈 연산자(*)가 먼저 수행됩니다.
        // 2. 그 다음 덧셈 연산자(+)와 뺄셈 연산자(-)가 수행됩니다.
        // 3. 등가 비교 연산자(==)가 그 다음으로 수행됩니다.
        // 4. AND 연산자(&&)가 수행됩니다.
        // 5. 마지막으로, 조건 연산자(? :)와 대입 연산자(=)가 수행됩니다.
        int num = (1 + 2 == 3 && 4 + 1 * 2 == 6) ? 3 + 4 : 5 + 6;

        // 연산 과정:
        // 1. 곱셈: 1 * 2 = 2
        // 2. 덧셈과 뺄셈: 1 + 2 = 3, 4 + 2 = 6
        // 3. 등가 비교: 3 == 3 -> true, 6 == 6 -> true
        // 4. AND 연산: true && true -> true
        // 5. 조건 연산: 조건이 true이므로 3 + 4 = 7이 최종 결과
        // 6. 대입 연산: num 변수에 7이 할당됩니다.

        System.out.println(num); // 최종 출력값: 7

        // 연산자 우선순위에 따라, 식은 단계별로 평가되고 최종 결과는 7이 됩니다.
        // 연산자 우선순위를 이해하면 복잡한 식도 단계별로 쉽게 해석할 수 있습니다.
    }
}
